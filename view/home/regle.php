<h1>A toutes et tous, bienvenus sur le Forum Espace !</h1>

<h3>Ce lieu est un espace d’échanges et de partages.</h3>
<h3>Voici quelques règles de bonne conduite à respecter lorsque vous utilisez le forum.</h3>

<h2>1. Les règles évidentes</h2> 

<p>– Politesse et savoir vivre : respectez les participants, utilisez des formules de politesse, ne vous énervez pas, …</br>

Consultez le paragraphe 3 « Politesse et savoir vivre »</br>

– Tous les propos diffamatoires, injurieux, insultants, racistes, obscènes … sont interdits sur le site.</br>
Consultez le paragraphe 4 « Les propos interdits sur le site » pour plus de détail.</br>

– Toutes les annonces commerciales, de vente de médicament, mensongères, de produits illégaux, de contrefaçon, sont strictement interdites sur le site.</p>

<h2>2. Le rôle des modérateurs</h2>

<p>Le rôle de nos modérateurs est de faire respecter les règles de bonne conduite pour rendre les espaces de discussions libres et agréables à tous, en adéquation avec les lois.
Les missions du modérateur sont :</br>

– Supprimer des messages ou des discussions ne respectant pas les règles de bonne conduite,</br>

– Supprimer les profils qui nuisent au bon fonctionnement du forum, par exemple via des messages insultants, des messages provocateurs, du prosélytisme religieux ou politique, etc.</br>

– Répondre à vos alertes ou demandes particulières,</br>

– Effacer tout propos interdit sur le site tel que par exemple des propos racistes, diffamatoires, insultants, xénophobes, pédophiles… Que ces propos soient explicites ou implicites, ils seront supprimés.</br>

– Retirer tout contenu texte, post, photo ou vidéo illicites</br>

– Retirer toutes petites annonces personnelles à but commercial.</br>

Les modérateurs interviennent a posteriori. Si un message vous semble contraire aux règles de bonne conduite énoncées ici, signalez-le aux modérateurs. Si cela ne suffit pas ou si le problème est plus complexe, merci de nous contacter.</p>

<h2>3. Politesse et savoir vivre</h2>

<p>Pour respecter la convivialité du forum et se sentir en confiance, nous vous recommandons :</br>

– De mettre les formes pour vous adresser à autrui.</br>
– Utilisez également des formules de politesse « Bonjour », « Merci », « S’il vous plait »,souvent hélas oubliées…</br>

– De ne pas vous énerver : si quelqu’un vous agace, la seule bonne conduite est d’ignorer ses messages.</br>

– De ne pas abuser de l’écriture en majuscules qui sont FAITES POUR CRIER.</br>

– De faire attention à l’orthographe et d’éviter d’utiliser le langage SMS, nuisible à une bonne compréhension des échanges.</br>
– Les injures ou les insultes sont évidemment interdites.</p>

<h2>4. Les propos interdits sur le site</h2>

<p>Les propos sur le site doivent respecter la morale et les lois en vigueur. Tous les propos insultants, obscènes, vulgaires, diffamatoires, racistes, xénophobes, extrémistes, pédophiles, menaçants certaines communautés, violents ou menaçants sont strictement interdits.</p>

<h2>5. Les principes fondamentaux</h2>

<p>Respectez et faites respecter les règles de bonne conduite, votre utilisation n’en sera que meilleure ! Si vous rencontrez un problème, n’hésitez pas à le signaler aux modérateurs.</p>
<p>* Données personnelles</br>

L’accès au forum est restreint aux seuls membres inscrits. Si vous diffusez vos coordonnées elles ne seront donc accessibles qu’aux adhérents de l’AFNP.</br>

* Règles d’exclusion</br>
Les modérateurs sont habilités à supprimer votre profil pour l’une des raisons suivantes :</br>

– Vous n’avez pas respecté les règles de bonne conduite</br>
– De nombreux internautes se sont plaints de votre conduite</br>
– Vous avez fait de la publicité ou du spam de façon abusive</br>
– Vous avez fait de la publicité interdite (commerciale, services payants, …)</br>
– Vous avez écrit et/ou tenu des propos interdits sur le site</br>

Toutes ces raisons sont graves, si vous ne respectez pas les règles de bonne conduite, votre profil sera immédiatement banni du forum.</br>

Partageons nos expériences et enrichissons-nous mutuellement !</p>